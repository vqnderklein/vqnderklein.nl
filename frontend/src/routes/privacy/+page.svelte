<h3>Contact</h3>
<p>
	Wanneer u ons contactformulier invult, vragen we om uw naam en e-mailadres. We gebruiken deze
	gegevens alleen om uw vraag te beantwoorden en bewaren ze veilig.
</p>
<p>
	Uw gegevens worden beveiligd verstuurd naar het contact e-mailadres van de beheeders, uw gegevens
	worden niet langer bewaard dan strikt noodzakelijk is, na 7 inactiviteit wordt de
	e-mailconversatie automatisch van onze servers verwijderd. Als u liever uw gegevens direct
	verwijderd wilt hebben kunt u dat aangeven tijdens de e-mailconversatie of een e-mail sturen naar
	hallo@vqnderklein.nl
</p>
